#include <stdio.h>
#include <limits.h>

int main()
{
    printf("\nmaximum value of char : %d\n", CHAR_MAX);
    printf("minimum value of char : %d\n", CHAR_MIN);

    printf("\nmaximum value of int : %d\n", INT_MAX);
    printf("minimum value of int : %d\n", INT_MIN);

    printf("\nmaximum value of long : %ld\n", LONG_MAX);
    printf("minimum value of long : %ld\n", LONG_MIN);

    printf("\nmaximum value of signed char : %d\n", SCHAR_MAX);
    printf("minimum value of signed char : %d\n", SCHAR_MIN);

    printf("\nmaximum value of short : %d\n", SHRT_MAX);
    printf("minimum value of short : %d\n", SHRT_MIN);

    printf("\nmaximum value of unsigned char : %u\n", UCHAR_MAX);

    printf("\nmaximum value of unsigned int : %u\n", UINT_MAX);

    printf("\nmaximum value of unsigned long : %lu\n", ULONG_MAX);

    printf("\nmaximum value of unsigned short : %u\n", USHRT_MAX);


    return 0;
}
